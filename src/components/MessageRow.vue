<template>
  <ContextMenu :options="actions" @update:value="handleAction">
    <div class="row">
      <div class="content">
        <Tags :ids="message.tagIds" />
        <div class="header">{{ message.header }}</div>
        <div
          :class="['text', { italic }]"
          v-if="textHTML"
          v-html="textHTML"
        ></div>
        <MessageEmbed
          v-if="message.downloadUrl"
          :url="message.downloadUrl"
          :inlineURL="message.inlineImageUrl"
          :fileName="message.fileName"
        />
        <div class="footer" v-if="message.footer">{{ message.footer }}</div>
      </div>
      <Vote
        :votes="message.votes"
        :type="message.topic?.votingType"
        :state="message.voteState"
        @vote="vote"
      />
    </div>
  </ContextMenu>
  <slot name="suffix" />
</template>

<script lang="ts" setup>
import ContextMenu from '@/components/ContextMenu.vue';
import MessageEmbed from '@/components/MessageEmbed.vue';
import Tags from '@/components/Tags.vue';
import Vote from '@/components/Vote.vue';
import linkify from 'linkifyjs/html';
import { useMessage } from 'naive-ui';
import { Message, VoteType } from 'studo.js';
import { computed } from 'vue';
import { useI18n } from 'vue-i18n';
import { useAction } from '../utils';

const props = defineProps<{
  message: Message;
}>();

const msg = useMessage();
const { t } = useI18n();

const italic = computed(() => /^<i>.+<\/i>$/.test(props.message.text));
const textHTML = computed(() => {
  // sanitize
  const span = document.createElement('span');
  const text = props.message.text.replace(/â˜º/g, ':)');
  span.textContent = italic.value ? text.substring(3, text.length - 4) : text;
  return linkify(span.innerHTML, {
    attributes: { target: '_blank', rel: 'noopenner noreferrer' },
  });
});
const actions = computed(() =>
  props.message.actionIds.map((id) => ({
    label: t('actions.' + id, id),
    value: id,
  }))
);
async function handleAction(action: string) {
  if ((await useAction(action, props.message)) === 'copied') {
    msg.info(t('copied'));
  }
}
async function vote(state: VoteType) {
  await props.message.vote(state);
}
</script>

